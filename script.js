// 学科が選択されたときに科目リストを更新する関数
function Subjects() {
    const courseS = document.getElementById('course');
    const subjectS = document.getElementById('subject');

    // 選ばれた学科の値を取得
    const selectedCourse = courseS.value;

    // 科目の選択肢をリセット
    subjectS.innerHTML = '<option value="">科目を選択してください</option>';

    // 選ばれた学科に対応する科目リストを追加
    if (subjectsB[selectedCourse]) {
        subjectsB[selectedCourse].forEach(subject => {
            const option = document.createElement('option');
            option.value = subject;
            option.textContent = subject;
            subjectS.appendChild(option);
        });
    }
}